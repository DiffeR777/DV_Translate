    public void TryReplaceString(String PACKAGE_NAME,String res_name, int resourceid) {
        try {
            XResources.setSystemWideReplacement(PACKAGE_NAME, "string", res_name, this.modRes.fwd(resourceid));
        } catch (Throwable localThrowable1) {
            XposedBridge.log("exception then try replace system string: " + res_name);
        }
    }

